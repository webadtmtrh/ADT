CREATE TABLE `facilities` (
  `id` int(32) NOT NULL AUTO_INCREMENT,
  `facilitycode` int(32) NOT NULL,
  `name` varchar(100) NOT NULL,
  `facilitytype` int(32) NOT NULL,
  `district` int(32) NOT NULL,
  `county` int(11) NOT NULL,
  `flag` int(1) NOT NULL DEFAULT '1',
  `email` varchar(50) NOT NULL,
  `phone` varchar(50) NOT NULL,
  `adult_age` int(11) NOT NULL,
  `weekday_max` varchar(10) NOT NULL,
  `weekend_max` varchar(10) NOT NULL,
  `supported_by` int(11) NOT NULL,
  `service_art` int(11) NOT NULL DEFAULT '0',
  `service_pmtct` int(11) NOT NULL DEFAULT '0',
  `service_pep` int(11) NOT NULL DEFAULT '0',
  `supplied_by` int(11) NOT NULL,
  `parent` varchar(10) NOT NULL,
  `map` int(11) NOT NULL,
  PRIMARY KEY (`id`),
  KEY `facilitycode` (`facilitycode`,`district`),
  KEY `facilitytype` (`facilitytype`)
) ENGINE=InnoDB DEFAULT CHARSET=latin1